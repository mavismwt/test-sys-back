package mwt.testsysback.entity;

public class Assign {
    private int assign_id;
    private String title;
    private String detail;
    private int score;
    private int weight;
    private String teachers;
    private String students;
    private String file_source;
    private String file_report;
    private String date_start;
    private String date_end;
    private String test_info;
    private String test_example;

    public int getAssign_id() {
        return assign_id;
    }

    public void setAssign_id(int assign_id) {
        this.assign_id = assign_id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public String getTeachers() {
        return teachers;
    }

    public void setTeachers(String teachers) {
        this.teachers = teachers;
    }

    public String getStudents() {
        return students;
    }

    public void setStudents(String students) {
        this.students = students;
    }

    public String getFile_source() {
        return file_source;
    }

    public void setFile_source(String file_source) {
        this.file_source = file_source;
    }

    public String getFile_report() {
        return file_report;
    }

    public void setFile_report(String file_report) {
        this.file_report = file_report;
    }

    public String getDate_start() {
        return date_start;
    }

    public void setDate_start(String date_start) {
        this.date_start = date_start;
    }

    public String getDate_end() {
        return date_end;
    }

    public void setDate_end(String date_end) {
        this.date_end = date_end;
    }

    public String getTest_info() {
        return test_info;
    }

    public void setTest_info(String test_info) {
        this.test_info = test_info;
    }

    public String getTest_example() {
        return test_example;
    }

    public void setTest_example(String test_example) {
        this.test_example = test_example;
    }
}
